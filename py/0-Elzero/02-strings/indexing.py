
s1 = "pipi is angry"

#indexing(access single item)
print(s1[0]) #we get 1st element in our string
print(s1[10]) #we get 11th element
print(s1[-1]) #we ll get last element
print(s1[-6]) #we ll get last 6th element

#slicing(access multiple sequence items)

print(s1[2:]) #from 3rd element to end 
print(s1[:12]) #from 1st to 11th element cuz we start from 0 same as C

#steps(select the increment number we move inside a string)

print(s1[0::2]) #so our index moves by 2 elements 
