#type() jst returns the type we use 

print(type(1)) #int
 
print(type(1.1)) #float

print(type("pipi")) #string

print(type(True)) #bool

print(type([2, 3, 5, 6, 7, 8])) #list(array)

print(type({"pipi": 4, "is here": 5})) #dict (!new{contain ints and str and we use {} instead of []})

print(type((2, 3, 6, 7, 8))) #tuple (new{same as array but we use () instead of []})