# we use a backslash \ kinda like C to escape \n(new line) \b(back space) etc
#\\ = escape backslash itself
#\' = escape single quotes
#\n = line feed
#\r = carriage return
#\t = horizontal tab
#\xhh = character hex value 
#... etc

print("pipi is\\ gone")
print("pipi is\' back")
print("pipi is\n gone")
print("pipi is\r gone")
print("pipi is\t gone")
print("pipi is \x4f\x73 gone")
